public class AddsModel {
    String id;
    String name;
    String mediatype;
    String url;
    String top_advertisement;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMediatype() {
        return mediatype;
    }

    public void setMediatype(String mediatype) {
        this.mediatype = mediatype;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTop_advertisement() {
        return top_advertisement;
    }

    public void setTop_advertisement(String top_advertisement) {
        this.top_advertisement = top_advertisement;
    }
}
